
import Foundation
import UIKit

public class Utils {
    
    /**
     screen height
     
     - returns: height
     */
    static func getScreenHeight() -> CGFloat {
        return UIScreen.mainScreen().bounds.height
    }
    
    /**
     screen width
     
     - returns: width
     */
    static func getScreenWidth() -> CGFloat {
        return UIScreen.mainScreen().bounds.width
    }
    
    
    
}
